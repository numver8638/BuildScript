def boolean_random(false_rate: float) -> bool:
    return random.random() <= false_rate


def generate_decode(file: TextIO, valid: Iterator[tuple[int, bytes]], invalid: Iterator[tuple[int, bytes]]) -> None:
    # Generate data
    chars = 0
    errors = 0
    data = []

    invalid_before = False
    for _ in range(random.randint(MIN_TEST_CHARS, MAX_TEST_CHARS)):
        if boolean_random(INVALID_DATA_RATIO) and not invalid_before:
            # Invalid character
            errors += 1
            data.append(next(invalid))
            invalid_before = True
        else:
            # Valid character
            chars += 1
            data.append(next(valid))
            invalid_before = False

    # Write generated data.
    file.write('const char RandomDecodeData[] = {\n')

    for _, encoded in data:
        for b in encoded:
            file.write(f"    '\\x{b:02X}',\n")

    file.write('};\n\n')

    file.write('const char32_t RandomDecodeExpects[] = {\n')

    for ch, _ in data:
        if ch == INVALID_CHARACTER:
            file.write('    Encoding::InvalidCharacter,\n')
        elif ch == INVALID_ENCODING:
            file.write('    Encoding::InvalidEncoding,\n')
        else:
            file.write(f"    static_cast<char32_t>(0x{ch:08X}),\n")

    file.write('};\n\n')

    file.write(f'constexpr size_t RandomDecodeExpectChars = {chars};\n')
    file.write(f'constexpr size_t RandomDecodeExpectErrors = {errors};\n')


def generate_encode(file: TextIO, valid: Iterator[tuple[int, bytes]], invalid: Iterator[tuple[int, bytes]]) -> None:
    # Generate data
    chars = 0
    errors = 0
    data = []

    invalid_before = False
    for _ in range(random.randint(MIN_TEST_CHARS, MAX_TEST_CHARS)):
        if boolean_random(INVALID_DATA_RATIO) and not invalid_before:
            # Invalid character
            errors += 1
            data.append(next(invalid))
            invalid_before = True
        else:
            # Valid character
            chars += 1
            data.append(next(valid))
            invalid_before = False

    # Write generated data
    file.write('const char32_t RandomEncodeData[] = {\n')

    for ch, _ in data:
        file.write(f"    static_cast<char32_t>(0x{ch:08X}),\n")

    file.write('};\n\n')

    file.write('const struct {\n'
               '    size_t Length;\n'
               '    char Data[8];\n'
               '} RandomEncodeExpects[] = {\n')

    for _, encoded in data:
        assert len(encoded) <= 8

        string = ', '.join([f"'\\x{b:02X}'" for b in encoded])
        file.write(f"    {{ {len(encoded)}, {{ {string} }} }},\n")

    file.write('};\n\n')

    file.write(f'constexpr size_t RandomEncodeExpectChars = {chars};\n')
    file.write(f'constexpr size_t RandomEncodeExpectErrors = {errors};\n')


