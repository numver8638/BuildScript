def unicode_valid(encoding: str) -> Iterator[tuple[int, bytes]]:
    """
    Create valid unicode character.

    :param encoding: the encoding.
    :return: a tuple of the character in unicode codepoint and bytes encoded in given encoding.
    """
    while True:
        if boolean_random(BASIC_PLANE_RATIO):
            ch = random.randrange(0x10000)

            if ch < 0xD800 or 0xDFFF < ch:
                yield ch, chr(ch).encode(encoding)
        else:
            ch = random.randint(0x10000, 0x110000)
            yield ch, chr(ch).encode(encoding)


def cp949_valid() -> Iterator[tuple[int, bytes]]:
    """
    Create valid unicode character representable in CP949.

    :return: a tuple of the character in unicode codepoint and bytes encoded in CP949.
    """
    while True:
        begin, end = random.choice(CP949_RANGES)
        ch = random.randint(begin, end)

        yield ch, chr(ch).encode('cp949')


def utf8_decode_invalid():
    """
    Generate invalid byte sequence of UTF-8 encoding.

    Case 1. an unexpected continuation character
    Case 2. a non continuation character before end of character.
    Case 3. an overlong encoding
    Case 4. a sequence that decodes to invalid code point.

    :return: a tuple of the character that represents invalid result and bytes of invalid UTF-8.
    """

    def unexpected_continuation():
        return INVALID_ENCODING, bytes([random.randint(0x80, 0xBF)])

    def non_continuation():
        if boolean_random(0.5):
            return INVALID_ENCODING, bytes([random.randint(0xE0, 0xEF), random.randint(0x80, 0xBF)])
        else:
            return INVALID_ENCODING, \
                   bytes([random.randint(0xF1, 0xF3), random.randint(0x80, 0xBF), random.randint(0x80, 0xBF)])

    def overlong_encoding():
        i = random.randint(0, 2)
        if i == 0:
            return INVALID_ENCODING, bytes([random.randint(0xC0, 0xC1), random.randint(0x80, 0xBF)])
        elif i == 1:
            return INVALID_ENCODING, bytes([0xE0, random.randint(0x80, 0x9F), random.randint(0x80, 0xBF)])
        else:  # i == 2
            return INVALID_ENCODING, \
                   bytes([0xF0, random.randint(0x80, 0x8F), random.randint(0x80, 0xBF), random.randint(0x80, 0xBF)])

    def to_invalid_codepoint():
        # U+D800 ~ U+DFFF
        return INVALID_CHARACTER, bytes([0xED, random.randint(0xA0, 0xBF), random.randint(0x80, 0xBF)])

    while True:
        yield random.choice([unexpected_continuation, non_continuation, overlong_encoding, to_invalid_codepoint])()


def utf16_decode_invalid(big_endian=False):
    """
    Create invalid byte sequence of UTF-16 encoding.

    Case 1. an unexpected low surrogate character.
    Case 2. not a low surrogate character after high surrogate character.

    :param big_endian: the endianness.
    :return: a tuple of the character that represents invalid result and bytes of invalid UTF-16.
    """

    def unexpected_low_surrogate():
        i = random.randint(0xDC00, 0xDFFF)

        if big_endian:
            return INVALID_ENCODING, bytes([(i >> 8) & 0xFF, i & 0xFF])
        else:
            return INVALID_ENCODING, bytes([i & 0xFF, (i >> 8) & 0xFF])

    def expect_low_surrogate():
        i = random.randint(0xD800, 0xDBFF)

        if big_endian:
            return INVALID_ENCODING, bytes([(i >> 8) & 0xFF, i & 0xFF])
        else:
            return INVALID_ENCODING, bytes([i & 0xFF, (i >> 8) & 0xFF])

    while True:
        yield random.choice([unexpected_low_surrogate, expect_low_surrogate])()


def utf32_decode_invalid(big_endian=False):
    """
    Create invalid byte sequence of UTF-32 encoding.

    Case 1. a character that grater than unicode range.
    Case 2. a character in surrogate range.

    :param big_endian: the endianness.
    :return: a tuple of the character that represents invalid result and bytes of invalid UTF-32.
    """

    def out_of_unicode():
        i = random.randint(0x110000, 0xFFFFFFFF)

        if big_endian:
            return INVALID_CHARACTER, bytes([(i >> 24) & 0xFF, (i >> 16) & 0xFF, (i >> 8) & 0xFF, i & 0xFF])
        else:
            return INVALID_CHARACTER, bytes([i & 0xFF, (i >> 8) & 0xFF, (i >> 16) & 0xFF, (i >> 24) & 0xFF])

    def in_surrogate():
        i = random.randint(0xD800, 0xDFFF)

        if big_endian:
            return INVALID_CHARACTER, bytes([(i >> 24) & 0xFF, (i >> 16) & 0xFF, (i >> 8) & 0xFF, i & 0xFF])
        else:
            return INVALID_CHARACTER, bytes([i & 0xFF, (i >> 8) & 0xFF, (i >> 16) & 0xFF, (i >> 24) & 0xFF])

    while True:
        yield random.choice([out_of_unicode, in_surrogate])()


def cp949_decode_invalid():
    """
    Create invalid byte sequence of CP949 encoding.

    Case 1. invalid byte.
    Case 2. an undefined trailing byte after lead byte.

    :return: bytes of invalid CP949.
    """

    def invalid_byte():
        return INVALID_ENCODING, bytes([random.choice([0x80, 0xC9, 0xFE, 0xFF])])

    def undefined_trailing_byte():
        return INVALID_ENCODING, bytes([random.randint(0x81, 0xFD), random.randint(0x00, 0x40)])

    while True:
        yield random.choice([invalid_byte, undefined_trailing_byte])()


def unicode_encode_invalid():
    """
    Generate invalid unicode data.

    :return: a tuple of invalid unicode codepoint and empty bytes.
    """
    def out_of_unicode():
        return random.randint(0x110000, 0xFFFFFFFF)

    def in_surrogate():
        return random.randint(0xD800, 0xDFFF)

    while True:
        yield random.choice([out_of_unicode, in_surrogate])(), bytes()


def cp949_encode_invalid():
    """
    Generate invalid CP949 data in unicode codepoint.

    :return: a tuple of invalid CP949 character in unicode codepoint and empty bytes.
    """
    def cannot_be_encoded():
        while True:
            try:
                ch = random.randrange(0x110000)
                chr(ch).encode('cp949')
            except UnicodeEncodeError:
                return ch
            else:
                continue

    def out_of_unicode():
        return random.randint(0x110000, 0xFFFFFFFF)

    def in_surrogate():
        return random.randint(0xD800, 0xDFFF)

    while True:
        yield random.choice([cannot_be_encoded, out_of_unicode, in_surrogate])(), bytes()


